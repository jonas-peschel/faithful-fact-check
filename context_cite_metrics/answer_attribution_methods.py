def get_results_metadata(cc: ContextCiter, res: dict):
    """Add key-structure to the results dict for saving meta-info and the attribution scores."""
    # check if metadata already exists from previous run, if yes then set the cc model answer / output
    if res.get("model_output_full"):
        cc._cache["output"] = res["model_output_full"]
    else:
        # split the answer in statements/sentences and get corresponding starting and ending indices
        answer_statements, char_spans = split_model_answer(cc)
        
        res["query"] = cc.query
        res["model_answer"] = cc.response.strip()
        res["answer_statements"] = answer_statements
        res["num_answer_statements"] = len(answer_statements)
        res["answer_statement_char_spans"] = char_spans
        res["model_output_full"] = cc._cache["output"]
        res["num_sources"] = len(cc.sources)  

    # attribution scores
    if "methods" not in res.keys():
        res["methods"] = {}          

    return res, cc

